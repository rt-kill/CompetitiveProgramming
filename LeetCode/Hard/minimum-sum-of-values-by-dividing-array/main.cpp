class Solution {
public:
    int minimumValueSum(vector<int>& nums, vector<int>& andValues) {
        // although not specified, for now will assume that the subarrays must also be increasing in indices
        //  we have that nums[0] must always contain andValues[0]
        //      that is some first and last position where it will equal andValues[0]
        //  the next subarray will either be strictly contained in this interval
        //      or this interval will have some suffix of elements all of which contain andValues[1]


        vvpi pairs( andValues.size(), vpi( nums.size(), {-1, -1} ) );

        int const MBIT=20, MAND=(1<<MBIT)-1;
        vi bit_cnt( 1<<MBIT, 0 );

        // O(10)
        for(int i=0; i<andValues.size(); ++i) {
            int v = andValues[i];
            vpi max_intvs;

            for(int li=0, ri; li<nums.sz; li=ri) {
                ri = li+1;
                if(( nums[li] & v ) != v) continue;

                int cv = nums[li];
                while(ri<nums.sz && (nums[ri] & v) == v) cv &= nums[ri++];
                if( cv == v ) max_intvs.pb({ li, ri });
            }

            // O(1e5)
            for(auto [l, r] : max_intvs) {
                // part of O(1e5)
                for( int li=l, ri=l, cv=MAND; li < r; ) {
                    while( ri < r && v != cv ) {
                        int nv = nums[ri++];

                        int nnv = (~nv) & MAND;
                        while( nnv > 0 ) {
                            int part = (nnv&-nnv);
                            ++bit_cnt[part];
                            nnv -= part;
                        }

                        cv &= nv;
                    }

                    if( v == cv ) pairs[i][li] = { ri-1, r };

                    // increment li and update cv
                    int pv = nums[li];

                    int npv = (~pv) & MAND;
                    while( npv > 0 ) {
                        int part = (npv&-npv);
                        if(--bit_cnt[part] == 0) cv |= part;
                        npv -= part;
                    }

                    ++li;
                }
            }
        }


        // let dp[i][j] be the solution to (nums[i:], andValues[j:])
        //  dp[i][j] = min{ k s.t. &(nums[i:k]) = andValues[j] } dp[k][j+1]
        //           = min{ k in [mv[i,j], Mv[i,j]] } dp[k][j+1]
        //           can be done in log(n) with range min query


        // suppose that pdp[i] is the solution for ( nums[i:], andValues[j+1:] )
        //  we want to populate cdp[i]

        int n = nums.sz, m = andValues.sz;

        vi pdp(n+1, INT_MAX/2), cdp(n+1);
        pdp[n] = 0;

        for(int j=m-1; j>=0; --j) {
            cdp[n] = INT_MAX/2;

            int cli=0, cri=0;
            multiset<int> vals;

            for(int i=0; i<n; ++i) {
                cdp[i] = INT_MAX/2;
                auto [mv, Mv] = pairs[j][i];
                if( mv == Mv ) continue;

                while(cri < Mv) {
                    vals.insert( nums[cri] + pdp[cri+1] );
                    ++cri;
                }

                while(cli < mv) {
                    vals.erase( vals.find( nums[cli] + pdp[cli+1] ) );
                    ++cli;
                }

                cdp[i] = *vals.begin();
            }

            swap(pdp, cdp);
        }

        return pdp[0] < INT_MAX/2 ? pdp[0] : -1;
    }
};
